#Objective: Revise String manipulation in Python via the lower(),upper() and
#title() methods

autobot = "Optimus"
decepticon = "megatron"
transformers = "robots in disguise"

lowercase_autobot = autobot.lower()
uppercase_decepticon = decepticon.upper()
titlecase_transformers = transformers.title()

#Expected outcome: optimus
print(lowercase_autobot)
#Expected outcome: MEGATRON
print(uppercase_decepticon)
#Expected outcome: Robots In Disguise
print(titlecase_transformers)
